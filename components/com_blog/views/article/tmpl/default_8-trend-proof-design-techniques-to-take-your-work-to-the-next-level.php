            <h3 class="mt+b">Less Really is More</h3>
            <p>Sounds like a cliche but ‘less is more’ could not apply more aptly than to the field of graphic design.</p>
            <p>Most graphic design is not just an artistic endeavour. It usually has a commercial goal it wants to achieve.</p>
            <p>And the simplest way to achieve your goal, in most cases, is to limit the elements you include in your design.</p>
            <p>The more you include on your canvas the more there is for an audience to absorb. And the more information their brain has to sift through, the more likely it is your intended message will get lost.</p>
            <p>Of course there are plenty of examples where designers have integrated a complex numbers of elements with effective results. This sort of ability is not easy and it only comes with time and patience. Just as often you will see some of the biggest brands on the planet producing great content with a minimalist approach.</p>
            <p>Apple and Google are two prominent cases where companies have taken a minimalist approach to their marketing visuals. Take a page out of their book and don’t let your design lose its message in the clutter.</p>
