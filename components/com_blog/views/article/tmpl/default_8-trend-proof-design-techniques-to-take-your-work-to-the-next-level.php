            <h3 class="mt+b">The Rule of Thirds</h3>
            <p>The rule of thirds is a part of basic design theory and has been referenced as early as the 18th century. Much has been said about it since then, but you don’t need a full rundown on its history and theory to get something out of it. You just need to know one thing - it works!</p>
            <p>And you don’t have to be genius designer to implement it in your work. All it requires is dividing the canvas you are working on into three equal sections, both vertically and horizontally. Then use these lines as a rule of thumb for laying out the most distinctive elements of your design. </p>
            <p>Search “rule of third examples” for a better idea of what the effect looks like.</p>
            <p>Even if you don’t understand precisely how it works, the rule of thirds is a straightforward, actionable technique to leverage generations of design theory into your own work, instantly.</p>
